    public int Dem() throws ClassNotFoundException {
        int dem = 0;
        try {
            Connect pt = new Connect();
            ResultSet rs = pt.GetData("sanpham");
            while (rs.next()) {
                dem++;//Đếm số dòng trong table Products
            }
        } catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }
        return dem;
    }
    public void ShowTable() throws ClassNotFoundException ,SQLException 
    {
        DefaultTableModel model = (DefaultTableModel) tbSanPham.getModel();
        model.setRowCount(0);
        model.setRowCount(Dem());
        DecimalFormat df = new DecimalFormat("#,###");
        try {
             Connect cn = new Connect();
             Statement statement = cn.con.createStatement();
             String sql = "SELECT MaSanPham, TenSanPham, TenNhaCungCap, GiaBan, SoLuongTon " +
                           "FROM sanpham "
                           + "JOIN nhacungcap  "
                           + "ON sanpham.MaNhaCungCap = nhacungcap.MaNhaCungCap  ";
             ResultSet rs = statement.executeQuery(sql);
             int i = 0;
            while (rs.next()) {                
                tbSanPham.setValueAt(rs.getString("MaSanPham"), i, 0);
                tbSanPham.setValueAt(rs.getString("TenSanPham"), i, 1);
                tbSanPham.setValueAt(rs.getString("TenNhaCungCap"), i, 2);
                tbSanPham.setValueAt( df.format(rs.getDouble("GiaBan")), i, 3);
                tbSanPham.setValueAt(rs.getString("SoLuongTon"), i, 4);
                i++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if(txtMaSanPham.getText() != null && txtTenSanPham.getText() != null && txtGiaBan.getText() != null && txtSoLuong.getText() != null && txtNhaCungCap.getText() != null)
            {
                
                Connect cn = new Connect();
                ResultSet rs = cn.GetData("nhacungcap");
                boolean check = false;
                
                while(rs.next())
                {
                      if(txtNhaCungCap.getText().equalsIgnoreCase(rs.getString("MaNhaCungCap")))
                      {
                          check = true;
                      }
                }
                if(check == false)
                {
                    JOptionPane.showMessageDialog(this, "Mã Nhà Cung Cấp Không Tồn Tại!!!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
                else
                {
                Statement st = cn.con.createStatement();
                String sql = "INSERT INTO sanpham VALUES ('" 
                + txtMaSanPham.getText() + "', '"
                + txtTenSanPham.getText() + "', '"
                + txtNhaCungCap.getText() + "', "
                + txtGiaBan.getText() + ", "
                + txtSoLuong.getText() + ", 'Đang Bán', "
                + txtGiaNhap.getText() + ")";
                st.executeUpdate(sql);
                }
                ShowTable();
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

